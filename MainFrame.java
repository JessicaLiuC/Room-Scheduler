    public void rebuildDropRoomComboBox(){
        ArrayList<String> room = new ArrayList<>();
        ArrayList<RoomEntry> rooms = Room.getAllPossibleRooms();
        for (int counter=0; counter< rooms.size(); counter++){
            room.add(rooms.get(counter).getName());
        }
        dropRoomComboBox.setModel(new javax.swing.DefaultComboBoxModel(room.toArray()));
    }

    
    public void putWaitlist(){
        ArrayList<WaitlistEntry> waitlist = Waitlist.getAllWaitlist();
        StatusWaitlistList.removeAllElements();
        for (int counter = 0; counter<waitlist.size(); counter++){
            StatusWaitlistList.addElement(waitlist.get(counter).getFaculty()+ " reserves "+ waitlist.get(counter).getSeats()+" seats on "+ waitlist.get(counter).getDate());
        }
        }
    
    
