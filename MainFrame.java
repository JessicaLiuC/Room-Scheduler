    private void cancelReservationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelReservationButtonActionPerformed
        // TODO add your handling code here:
        String name = cancelFacultyComboBox.getSelectedItem().toString();
        Object dates = cancelDateComboBox.getSelectedItem();
        Date date = Date.valueOf(dates.toString());
        Reservations.cancelReservation(name, date);
        Waitlist.deleteWaitlist(name, date); //if cancel reservation is in waitlist
        
        
        //check if the room is available for waitlist
        ArrayList<RoomEntry> rooms = Room.getAllPossibleRooms();
        ArrayList<RoomEntry> reserveRoom = Reservations.getRoomsReservedByDate(date);
        ArrayList<String> reserveRoomString = new ArrayList<>();
        ArrayList<WaitlistEntry> waitlist = Waitlist.getAllWaitlist();
        Timestamp timestamp = new Timestamp(Calendar.getInstance().getTime().getTime());
        
        if (!waitlist.isEmpty()){
            for (int counter = 0; counter<reserveRoom.size();counter++){
                reserveRoomString.add(reserveRoom.get(counter).getName());
            }


            for (int counter=0; counter<1; counter++){
                RoomEntry currentRooms = rooms.get(counter);
                String faculty = waitlist.get(counter).getFaculty();
                Date Date = waitlist.get(counter).getDate();
                int Seat = waitlist.get(counter).getSeats();

                if (!reserveRoomString.contains(waitlist.get(counter).getDate())){
                    Reservations.addReservationEntry(new ReservationEntry(faculty, currentRooms.getName(), Date, Seat,timestamp));
                    newReserveLabel.setText(faculty+" gets room "+ currentRooms.getName()+ " reserved");
                    Waitlist.deleteWaitlist(faculty, Date);
                    System.out.println("yes");
                }

            }
        }
        cancelLabel.setText(name+" "+date+ " gets cancelled");
        putWaitlist();
        if(StatusWaitlistList.isEmpty()){
            cancelLabel.setText(name+" gets cancelled from waitlist");
            newReserveLabel.setText("");
        }
    }//GEN-LAST:event_cancelReservationButtonActionPerformed

