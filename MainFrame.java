    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        // TODO add your handling code here:
        Object names = reserveFacultyComboBox.getSelectedItem();
        Object dates = reserveDateComboBox.getSelectedItem();
        String seats = reserveSeatsTextField.getText();
        Timestamp timestamp = new Timestamp(Calendar.getInstance().getTime().getTime());
        
        String name = names.toString(); //change from object to string
        Date date = Date.valueOf(dates.toString()); //change from object to string
        int seat = Integer.parseInt(seats); //change from string to integer
        
        ArrayList<RoomEntry> rooms = Room.getAllPossibleRooms();
        ArrayList<RoomEntry> reserveRoom = Reservations.getRoomsReservedByDate(date);
        ArrayList<String> reserveRoomString = new ArrayList<>();
        
        for (int counter = 0; counter<reserveRoom.size();counter++){
            reserveRoomString.add(reserveRoom.get(counter).getName());
        }
       
        
        for (int counter = 0; counter<rooms.size();counter++){
            RoomEntry currentRooms = rooms.get(counter);
            if (currentRooms.getSeats()>=seat){
                if (!reserveRoomString.contains(currentRooms.getName())){
                    Reservations.addReservationEntry(new ReservationEntry(name, currentRooms.getName(),date,seat,timestamp));
                    reservationLabel.setText(name+" reserves room "+ currentRooms.getName());
                    return;
                }
            }
        }        
        
        Waitlist.addWaitlistEntry(name, date, seat);
        reservationLabel.setText(name+" gets waitlisted.");
        putWaitlist();
        
    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

