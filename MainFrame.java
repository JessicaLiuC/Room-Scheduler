    private void dropRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropRoomButtonActionPerformed
        // TODO add your handling code here:
        String room = dropRoomComboBox.getSelectedItem().toString();
        Room.dropRoom(room);
        dropStatusLabel.setText("Room "+room+" has been dropped");
        
        //see if waitlist or another room
        ArrayList<ReservationEntry> reservation = Reservations.getReservationByRoom(room);
        ArrayList<RoomEntry> rooms = Room.getAllPossibleRooms();
        
        for(int counter = 0; counter< reservation.size(); counter++){
            ReservationEntry currentRooms = reservation.get(counter);
            Reservations.deleteReservation(currentRooms.getRoom());
            
            Waitlist.addWaitlistEntry(currentRooms.getFaculty(), currentRooms.getDate(), currentRooms.getSeats());
            dropRoomStatusTextArea.append("\n"+currentRooms.getFaculty()+" "+currentRooms.getDate()+" is moved to waitlist.");
            
            ArrayList<ReservationEntry> reservationByDate = Reservations.getStatusReservationsByDate(currentRooms.getDate());
            ArrayList<String> reservationString = new ArrayList<>();
            for(int count=0; count<reservationByDate.size(); count++){
                reservationString.add(reservationByDate.get(count).getRoom());
            }
            
            int seats = currentRooms.getSeats();
            for (int i = 0; i<rooms.size(); i++){
                RoomEntry current = rooms.get(i);
                if (current.getSeats()>= seats){
                    if(!reservationString.contains(current.getName())){
                        Reservations.addReservationEntry(new ReservationEntry(currentRooms.getFaculty(), current.getName(), currentRooms.getDate(), seats, currentRooms.getTimestamp()));
                        dropRoomStatusTextArea.append("\n"+ currentRooms.getFaculty()+" reserves room "+ current.getName()+" on "+ currentRooms.getDate()+" from waitlist.");
                        Waitlist.deleteWaitlist(currentRooms.getFaculty(), currentRooms.getDate());
                        break;
                    }
                }
            }
        }
        rebuildDropRoomComboBox();
        putWaitlist();
    }//GEN-LAST:event_dropRoomButtonActionPerformed

